'''
Задача 3: Полиморфная функция с isinstance()

Условие:
Это классическая задача на полиморфизм. Напишите функцию, которая по-разному о
брабатывает объекты разных классов.

Вам нужно:

    Создать три класса: Shape (Фигура), Circle(Shape) (Круг) и Square(Shape) 
    (Квадрат). Тела классов могут быть пустыми.

    Написать полиморфную функцию get_shape_type(shape_object), которая:

        Принимает на вход объект shape_object.

        Если объект является экземпляром Circle, возвращает строку "Это круг".

        Если объект является экземпляром Square, возвращает строку "Это квадрат".

        Если объект является экземпляром Shape, но не Circle и не Square, 
        возвращает строку "Это общая фигура".

        Используйте isinstance() и if/elif/else.
'''

# Определение базового класса Figure (Фигура)
# Пока что класс пустой — используется только для наследования
class Shape:
    pass

# Класс Circle (Круг), наследуется от Shape
# Представляет круг как частный случай фигуры
class Circle(Shape):
    pass

# Класс Square (Квадрат), наследуется от Shape
# Представляет квадрат как частный случай фигуры
class Square(Shape):
    pass

# Полиморфная функция, определяющая тип фигуры по её экземпляру
def get_shape_type(shape_object):
    # Проверяем, является ли объект экземпляром Circle
    if isinstance(shape_object, Circle):
        return "Это круг"
    # Проверяем, является ли объект экземпляром Square
    elif isinstance(shape_object, Square):
        return "Это квадрат"
    # Проверяем, является ли объект просто экземпляром Shape (но не Circle/Square)
    elif isinstance(shape_object, Shape):
        return "Это общая фигура"
    # Добавим обработку случая, если передан объект другого типа
    else:
        return "Неизвестный тип фигуры"

'''Комментарии к коду:

Все классы объявлены как пустые (pass), так как в задаче требуется только
 структура наследования.
Функция get_shape_type() использует isinstance() — это правильный способ проверки 
типа в Python, особенно при работе с наследованием.
Добавлена дополнительная проверка в конце (else), чтобы избежать ситуации, когда 
передан объект не связанной иерархии (например, строка или число). 
Это делает функцию более безопасной.
Порядок проверок важен: сначала идут более специфичные типы (Circle, Square), 
затем общий (Shape). Это корректная практика при использовании isinstance 
в цепочке if-elif.'''