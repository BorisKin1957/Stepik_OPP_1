'''
Задача 4: Доступ к атрибутам

Условие:
Напишите метод, который использует как атрибуты экземпляра,
так и атрибуты класса.

Вам нужно:

    Создать класс Employee (Сотрудник).

    Определить атрибут класса company со значением "Stepik".

    В __init__ принять два аргумента, name и position (должность),
    и сохранить их в атрибуты экземпляра.

    Создать метод get_info(), который возвращает строку в формате:
    "[name] работает в компании [company] на должности [position].".
    Метод должен использовать и атрибуты экземпляра, и атрибут класса.
'''

class Employee:
    # Атрибут класса, общий для всех экземпляров
    company = 'Stepik'

    # Конструктор, инициализирует атрибуты экземпляра
    def __init__(self, name, position):
        self.name = name         # Атрибут экземпляра: имя сотрудника
        self.position = position # Атрибут экземпляра: должность сотрудника

    # Метод, использующий атрибуты экземпляра и атрибут класса
    def get_info(self):
        # Формирует строку с информацией о сотруднике
        return f'{self.name} работает в компании {Employee.company} на должности {self.position}.'

