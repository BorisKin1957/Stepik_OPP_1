'''Задача 1: Простой геттер
Условие:
Ваша задача — создать "официальный" способ для чтения защищенного атрибута.

Вам нужно:

Создать класс Account.

В __init__ принять один аргумент balance и сохранить его в защищенный атрибут self._balance.

Создать публичный метод-геттер get_balance(), который возвращает значение self._balance.'''

class Account:
    def __init__(self, balance):
        self._balance = balance  # Инициализация защищенного атрибута

    def get_balance(self):
        return self._balance  # Публичный геттер для безопасного доступа к _balance

'''Комментарии:

self._balance — подчёркивание в начале имени атрибута указывает, что он защищённый 
(не предназначен для прямого доступа извне по соглашению).
get_balance() — это геттер, предоставляющий контролируемый доступ к значению _balance. 
Это соответствует принципам инкапсуляции.
Код корректен, прост и соответствует требованиям задачи.'''