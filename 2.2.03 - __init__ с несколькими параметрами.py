'''
Задача 3: __init__ с несколькими параметрами

Условие:
Закрепим навык, используя несколько параметров.

Вам нужно:

    Создать класс User.

    Определить в нем __init__, который принимает два параметра: username и age.

    В конструкторе создать два атрибута экземпляра: self.username и self.age,
    присвоив им соответствующие значения.

    Создать метод get_info(), который возвращает строку в формате:
    "Имя: [username], Возраст: [age]".
'''

class User:
    # Конструктор класса, принимающий два параметра: username и age
    def __init__(self, username, age):
        # Присваиваем переданное имя пользователя атрибуту экземпляра self.username
        self.username = username
        # Присваиваем переданный возраст атрибуту экземпляра self.age
        self.age = age

    # Метод, возвращающий строку с информацией о пользователе
    def get_info(self):
        # Формируем и возвращаем строку в нужном формате, используя f-строку
        return f'Имя: {self.username}, Возраст: {self.age}'
