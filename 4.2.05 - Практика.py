'''
Задача 5: Практика: метод для расчета цены со скидкой

Условие:
Давайте используем унаследованные и новые атрибуты для вычислений.

Вам нужно:

    Использовать классы Product и DiscountedProduct из Задачи 3.

    В дочерний класс DiscountedProduct добавить новый метод get_price_with_discount().

    Этот метод должен:

        Получить доступ к self.price (унаследованному от Product).

        Получить доступ к self.discount (собственному атрибуту DiscountedProduct).

        Рассчитать и вернуть итоговую цену по формуле: цена - (цена * скидка / 100).
'''

# Базовый класс "Товар"
class Product:
    # Инициализация товара с именем и ценой
    def __init__(self, name, price):
        self.name = name      # Устанавливаем название товара
        self.price = price    # Устанавливаем стоимость товара


# Дочерний класс "Товар со скидкой", наследующийся от Product
class DiscountedProduct(Product):
    # Инициализация товара со скидкой
    def __init__(self, name, price, discount):
        super().__init__(name, price)  # Вызываем конструктор родительского класса
        self.discount = discount       # Добавляем атрибут скидки (в процентах)

    # Метод для расчета цены с учетом скидки
    def get_price_with_discount(self):
        # Формула: цена - (цена * скидка / 100)
        # Эквивалентно: цена * (1 - скидка / 100)
        return self.price * (1 - self.discount / 100)