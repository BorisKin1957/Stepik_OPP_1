'''
Задача 1: Полиморфизм через наследование

Условие:
Ваша задача — реализовать классический полиморфизм через наследование и 
переопределение методов.

Вам нужно:

    Создать базовый класс Document с методом display(), который возвращает 
    строку "Отображение документа".

    Создать два дочерних класса, PdfDocument(Document) и WordDocument(Document).

    В каждом дочернем классе переопределить метод display():

        PdfDocument.display() должен возвращать "Отображение PDF документа".

        WordDocument.display() должен возвращать "Отображение Word документа".

Шаблон кода создаст объекты и проверит их полиморфное поведение.'''

class Document:
    def display(self):
        # Базовая реализация метода отображения документа
        return 'Отображение документа'


class PdfDocument(Document):
    def display(self):
        # Переопределение метода для PDF-документа
        return 'Отображение PDF документа'


class WordDocument(Document):
    def display(self):
        # Переопределение метода для Word-документа
        return 'Отображение Word документа'

'''Пояснение:
Каждый класс наследуется от Document, и метод display() переопределяется в 
подклассах. Это позволяет использовать полиморфизм: один и тот же интерфейс 
(display()) ведёт себя по-разному в зависимости от типа объекта.
 Комментарии поясняют назначение каждого метода.'''