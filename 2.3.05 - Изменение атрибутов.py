'''
Задача 5: Изменение атрибутов

Условие:
Это продвинутая задача, которая проверит ваше глубокое понимание разницы
между атрибутами класса и экземпляра.

Вам нужно:

    Создать класс Config.

    Определить атрибут класса theme со значением "light".

    В __init__ принимать один аргумент app_name и сохранять его в атрибут экземпляра.

    Создать метод get_theme(), который возвращает текущую тему. Он должен обращаться к self.theme.
'''

class Config:
    # Атрибут класса, общий для всех экземпляров
    theme = "light"

    # Конструктор, инициализирует атрибут экземпляра — имя приложения
    def __init__(self, app_name):
        self.app_name = app_name  # Атрибут экземпляра

    # Метод возвращает значение theme через self, что позволяет
    # получать сначала значение экземпляра (если задано), иначе — класса
    def get_theme(self):
        return self.theme


# === Пример использования и изменение темы у экземпляра ===

# Создаём экземпляр класса Config
config1 = Config("Мой блокнот")

# Выводим текущую тему (будет "light", так как используется атрибут класса)
print(config1.get_theme())  # Вывод: light

# Меняем тему НЕПОСРЕДСТВЕННО у ЭКЗЕМПЛЯРА (создаём атрибут экземпляра с именем theme)
config1.theme = "dark"  # Теперь экземпляр имеет собственный атрибут theme

# Выводим тему снова — теперь будет "dark", потому что self.theme найдёт атрибут у экземпляра
print(config1.get_theme())  # Вывод: dark

# Проверим, что атрибут класса остался неизменным
print(Config.theme)  # Вывод: light (класс по-прежнему имеет theme = "light")

# Создадим новый экземпляр — он унаследует theme = "light" по умолчанию
config2 = Config("Калькулятор")
print(config2.get_theme())  # Вывод: light