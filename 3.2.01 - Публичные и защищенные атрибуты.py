'''Задача 1: Публичные и защищенные атрибуты
Условие:
Ваша задача — правильно распределить уровни доступа для атрибутов класса.

Вам нужно создать класс AppSettings (Настройки приложения):

В методе __init__ он должен принимать user_id и theme.

user_id — это публичная информация, сохраните ее в публичный атрибут self.user_id.

theme — это внутренняя настройка, которую не следует менять напрямую,
сохраните ее в защищенный атрибут self._theme.

Шаблон кода проверит, что вы правильно назвали атрибуты.'''


class AppSettings:
    # Конструктор класса, принимает два параметра: user_id и theme
    def __init__(self, user_id, theme):
        # Публичный атрибут: user_id доступен извне, используется как открытая информация
        self.user_id = user_id
        # Защищённый атрибут: _theme обозначается с подчёркиванием, чтобы показать,
        # что это внутренняя настройка, которую не следует изменять напрямую
        self._theme = theme