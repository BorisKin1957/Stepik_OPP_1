'''
Задача 2: Простое расширение с super()

Условие:
Теперь давайте не заменять, а дополнять родительскую логику.

Вам нужно:

    Создать родительский класс Logger с методом log(message),
    который возвращает строку в формате "[LOG]: [message]" ( [LOG]: -
    это не переменная это строка которую надо указать напрямую)

    Создать дочерний класс TimestampLogger, который наследует от Logger.

    В классе TimestampLogger переопределить метод log(message). Новая версия должна:

        Сначала вызвать родительскую версию метода с помощью super() и
        получить базовую строку лога.

        Затем добавить к этой строке текущее время в формате " (timestamp)"
        ( просто добавьте эту строку один в один " (timestamp)" ).

        Вернуть итоговую строку, например: "[LOG]: Сервер запущен (timestamp)".
        (Для простоты используйте именно строку "(timestamp)").
'''

class Logger:
    def log(self, message):
        # Возвращает базовую строку лога с префиксом [LOG]:
        return f'[LOG]: {message}'

class TimestampLogger(Logger):
    def log(self, message):
        # Вызываем родительский метод через super(), чтобы получить базовую строку
        base_log = super().log(message)
        # Добавляем к результату фиксированную строку с меткой времени
        return f"{base_log} (timestamp)"


# Создаём экземпляр обычного логгера
logger = Logger()
# Создаём экземпляр логгера с меткой времени
timestamp = TimestampLogger()

# Печатаем результаты работы обоих логгеров
print(logger.log('hello'))      # Ожидается: [LOG]: hello
print(timestamp.log('hello'))   # Ожидается: [LOG]: hello (timestamp)

'''Код полностью соответствует условию задачи. Метод log в TimestampLogger 
расширяет функциональность родительского класса с помощью super(), 
а не заменяет её. Комментарии поясняют назначение каждого блока кода.'''