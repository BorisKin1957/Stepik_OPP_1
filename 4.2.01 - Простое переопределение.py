# Родительский класс Device
class Device:
    # Метод power_on возвращает сообщение о включении устройства
    def power_on(self):
        return 'Устройство включено'


# Дочерний класс Computer, наследующий от Device
class Computer(Device):
    # Переопределение метода power_on — замена поведения родительского метода
    # Теперь при вызове power_on() у экземпляра Computer будет возвращаться другая строка
    def power_on(self):
        return 'Компьютер загружается..'

'''Пояснение:

Класс Computer наследует от Device, но переопределяет метод power_on().
Это означает, что когда вызывается computer.power_on(), будет использоваться 
реализация из Computer, а не из Device.
Такой механизм называется переопределением методов (method overriding) и 
является ключевой частью полиморфизма в ООП.'''

print(Computer().power_on())