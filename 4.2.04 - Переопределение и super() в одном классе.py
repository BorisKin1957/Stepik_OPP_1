'''Задача 4: Переопределение и super() в одном классе

Условие:
Один дочерний класс может и заменять, и расширять разные методы.

Вам нужно создать иерархию Vehicle -> Car.

    Vehicle:

        __init__ принимает brand.

        start_engine() возвращает строку "Двигатель запущен".

        honk() (сигналить) возвращает строку "Общий сигнал!".

    Car:

        Наследует от Vehicle.

        Расширяет start_engine(). Сначала вызывает родительскую версию,
        а затем возвращает итоговую строку "Двигатель запущен... Проверка систем автомобиля.".

        Полностью переопределяет honk(), чтобы он возвращал строку "Би-бип!"'''

# Базовый класс Транспортное средство
class Vehicle:
    # Инициализация объекта с указанием бренда
    def __init__(self, brand):
        self.brand = brand  # Сохраняем бренд транспортного средства

    # Метод для запуска двигателя — возвращает сообщение
    def start_engine(self):
        return 'Двигатель запущен'

    # Метод сигнала — общий звук для всех транспортных средств
    def honk(self):
        return 'Общий сигнал!'


# Дочерний класс Машина, наследующий от Vehicle
class Car(Vehicle):
    # Переопределение метода start_engine с использованием super()
    def start_engine(self):
        old = super().start_engine()  # Вызываем родительский метод
        return f'{old}... Проверка систем автомобиля.'  # Добавляем дополнительное действие

    # Полная замена метода honk — больше не используем родительскую версию
    def honk(self):
        return 'Би-бип!'  # У машины свой уникальный сигнал

