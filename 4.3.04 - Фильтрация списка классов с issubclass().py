'''
Задача 4: Фильтрация списка классов с issubclass()

Условие:
Вам нужно написать функцию, которая отфильтрует список классов, оставив только
наследников определенного базового класса.

Вам нужно:

    Создать иерархию классов: Exception (встроенный) -> NetworkError(Exception)
    -> HttpError(NetworkError). Тела классов могут быть пустыми.

    Написать отдельную функцию get_network_error_classes(classes_list), которая:

        Принимает на вход список классов classes_list.

        Возвращает новый список, содержащий только те классы из classes_list,
        которые являются подклассами NetworkError.
'''

# Определяем класс NetworkError, который наследуется от встроенного класса Exception
class NetworkError(Exception):
    pass  # Пустое тело класса — используется по условию задачи

# Определяем класс HttpError, который наследуется от NetworkError
class HttpError(NetworkError):
    pass  # Пустое тело класса — используется по условию задачи

# Функция для фильтрации списка классов: оставляет только подклассы NetworkError
def get_network_error_classes(classes_list):
    # Проходим по каждому элементу в списке classes_list
    # Проверяем, что элемент является типом (т.е. классом, а не экземпляром)
    # И проверяем, является ли этот класс подклассом NetworkError (включая сам NetworkError)
    return [cls for cls in classes_list if isinstance(cls, type) and issubclass(cls, NetworkError)]


'''Пояснение:

isinstance(cls, type) — проверяет, что элемент списка — это класс (а не объект).
issubclass(cls, NetworkError) — проверяет, что класс cls является наследником 
NetworkError (включая сам NetworkError).
Генератор списка возвращает только те классы, которые удовлетворяют обоим условиям.'''