'''Задача 3: Приватный атрибут
Условие:
Иногда нужно обеспечить более сильную защиту от случайного изменения,
особенно при наследовании. Для этого используют приватные атрибуты.

Вам нужно создать класс APIClient:

В __init__ он должен принимать api_key.

Сохраните api_key в приватный атрибут self.__api_key.

Шаблон кода проверит, что атрибут действительно стал приватным и недоступен напрямую.'''

class APIClient:
    # Конструктор класса, принимает параметр api_key
    def __init__(self, api_key):
        # Создаём приватный атрибут __api_key, который недоступен напрямую извне класса
        # Двойное подчёркивание в начале имени делает атрибут приватным (name mangling)
        self.__api_key = api_key