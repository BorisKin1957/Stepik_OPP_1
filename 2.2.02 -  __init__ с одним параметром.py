'''
Задача 2: __init__ с одним параметром

Условие:
Теперь научим наш конструктор принимать данные извне.

Вам нужно:

    Создать класс Book.

    Определить в нем метод __init__, который принимает один параметр
    (кроме self): title.

    Внутри __init__ создать атрибут экземпляра self.title и присвоить ему
    значение из параметра title.

    Создать метод get_title(), который возвращает значение self.title.
'''

class Book:
    # Конструктор класса, принимает параметр title (по умолчанию — пустая строка)
    def __init__(self, title=''):
        # Присваиваем переданное значение title атрибуту экземпляра self.title
        self.title = title

    # Метод для получения значения атрибута title
    def get_title(self):
        # Возвращает текущее значение self.title
        return self.title
