'''Задача 4: Все уровни доступа в одном классе
Условие:
Давайте соберем все уровни доступа в одном классе, чтобы закрепить материал.

Создайте класс Component:

В __init__ он должен принимать name, version и _id.

name (имя компонента) — публичный атрибут.

_id (внутренний ID) — защищенный атрибут self._id.

version (версия) — приватный атрибут self.__version.'''

class Component:
    """
    Класс Component демонстрирует все уровни доступа к атрибутам в Python:
    публичные, защищённые и приватные.
    """

    def __init__(self, name, version, _id):
        # Публичный атрибут: доступен извне напрямую
        self.name = name

        # Приватный атрибут: доступен только внутри класса (через name mangling)
        self.__version = version

        # Защищённый атрибут: по соглашению не должен использоваться вне класса и его наследников
        self._id = _id

'''self.name — публичный атрибут. К нему можно свободно обращаться и изменять извне.
self.__version — приватный атрибут. Python переименовывает его в _Component__version, 
чтобы ограничить прямой доступ извне.
self._id — защищённый атрибут. Не запрещён к доступу, но сигнализирует, 
что это внутренняя реализация.'''