# Определение класса LightSwitch (выключатель света)
class LightSwitch:
    # Конструктор класса — вызывается при создании нового экземпляра
    def __init__(self):
        # Защищённый атрибут (по соглашению) хранит состояние выключателя
        # Изначально выключатель находится в положении "выключен" (False)
        self._is_on = False

    # Публичный метод toggle — "переключить состояние"
    # Используется для изменения состояния выключателя на противоположное
    def toggle(self):
        # Меняем значение self._is_on: если было True — станет False, и наоборот
        self._is_on = not self._is_on

    # Геттер (метод-получатель) для состояния выключателя
    # Позволяет безопасно узнать текущее состояние, не обращаясь напрямую к атрибуту
    def is_on(self):
        # Возвращает текущее значение внутреннего состояния (True или False)
        return self._is_on