'''
Задача 4: Полиморфизм встроенных операторов

Условие:
Полиморфизм в Python работает и для встроенных функций. Давайте это продемонстрируем.

Вам нужно написать одну функцию get_count(data_structure), которая:

    Принимает на вход структуру данных (data_structure).

    Возвращает ее длину, используя встроенную функцию len().

    Функция должна одинаково хорошо работать и со строками, и со списками, и со словарями.
'''

def get_count(data_structure):
    # Проверяем, является ли переданная структура данными одного из допустимых типов: dict, list, str
    if isinstance(data_structure, (dict, list, str)):
        # Если да — возвращаем длину с помощью полиморфной функции len()
        return len(data_structure)
    else:
        # Если нет — сообщаем, что тип не поддерживается
        return f'{data_structure} не относится к строкам, спискам, словарям'

# Пример вызова функции с различными типами данных
print(get_count([1, 2, 3]))  # Список: длина = 3
print(get_count({'a': 1, 'b': 2, 'c': 3}))  # Словарь: длина = 3 (количество ключей)
print(get_count('Здравствуй, попа - Новый год!'))  # Строка: длина = количество символов

# Пример с некорректным типом данных
print(get_count(123))  # Число не поддерживается — будет сообщение об ошибке


'''Пояснение:
Функция get_count() демонстрирует полиморфизм — она использует одну и ту 
же встроенную функцию len(), которая по-разному работает в зависимости от типа объекта:

Для списка — количество элементов.
Для словаря — количество ключей.
Для строки — количество символов.
Проверка через isinstance обеспечивает безопасность, чтобы избежать ошибок 
на неподдерживаемых типах.'''