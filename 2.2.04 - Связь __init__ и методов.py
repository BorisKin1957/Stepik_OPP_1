'''
Задача 4: Связь __init__ и методов

Условие:
Давайте посмотрим, как обычные методы могут использовать атрибуты, созданные в __init__.

Вам нужно:

    Создать класс Dog.

    В __init__ принять один параметр name и сохранить его в атрибут self.name.

    Создать метод bark(), который возвращает строку в формате:
    "[name] лает: Гав-гав!". Метод должен использовать атрибут self.name.
'''

class Dog:
    def __init__(self, name):
        # Инициализация атрибута экземпляра name при создании объекта
        self.name = name

    def bark(self):
        # Метод использует атрибут self.name, созданный в __init__
        return f"{self.name} лает: Гав-гав!"

