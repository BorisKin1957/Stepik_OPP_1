'''
Задача 2: Утиная типизация (Duck Typing)

Условие:
Теперь реализуйте полиморфизм без наследования, используя "утиную типизацию".

Вам нужно:

    Создать два независимых класса: Fish и Bird. У них не должно быть общего родителя.

    В классе Fish определить метод move(), который возвращает строку "Я плыву".

    В классе Bird определить метод move(), который возвращает строку "Я лечу".

    Написать полиморфную функцию make_it_move(creature), которая принимает 
    любой объект и возвращает результат вызова его метода move().
'''

# Класс Fish — представляет рыбу, которая может двигаться
class Fish:
    # Метод move возвращает строку, описывающую способ передвижения рыбы
    def move(self):
        return 'Я плыву'

# Класс Bird — представляет птицу, которая может двигаться
class Bird:
    # Метод move возвращает строку, описывающую способ передвижения птицы
    def move(self):
        return 'Я лечу'

# Полиморфная функция, которая работает с любым объектом, имеющим метод move()
# Это пример утиной типизации: "если это выглядит как утка и крякает как утка — значит, это утка"
def make_it_move(creature):
    # Вызываем метод move() у переданного объекта, не проверяя его тип
    return creature.move()