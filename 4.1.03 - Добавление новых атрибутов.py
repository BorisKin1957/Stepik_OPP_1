'''Задача 3: Добавление новых атрибутов
Условие:
Часто дочернему классу нужно не только новое поведение, но и новые данные.

Вам нужно:

Создать родительский класс Person с __init__, который принимает name и сохраняет его.

Создать дочерний класс Student, который наследует от Person.

Student.__init__ должен принимать name и student_id.

Внутри Student.__init__ нужно:

Правильно вызвать родительский __init__ с помощью super(), чтобы сохранить name.

Создать новый атрибут экземпляра self.student_id.'''

class Person:
    def __init__(self, name):
        self.name = name  # Сохраняем имя человека как атрибут экземпляра

class Student(Person):
    def __init__(self, name, student_id):
        super().__init__(name)  # Вызываем конструктор родительского класса Person для инициализации атрибута name
        self.student_id = student_id  # Добавляем новый атрибут student_id, специфичный только для класса Student


'''Пояснение:

Класс Person — базовый класс, который хранит имя человека.
Класс Student наследуется от Person и расширяет функциональность, добавляя 
идентификатор студента.
super().__init__(name) — корректный способ вызова метода __init__ родительского 
класса, чтобы не дублировать логику.
self.student_id = student_id — добавление нового атрибута, характерного 
именно для дочернего класса.'''