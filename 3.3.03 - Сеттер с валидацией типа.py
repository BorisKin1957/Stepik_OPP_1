

class Mailbox:
    # Конструктор класса — инициализирует объект, устанавливая приватный атрибут _owner в None
    def __init__(self):
        self._owner = None  # Атрибут для хранения владельца почтового ящика

    # Геттер: возвращает текущее значение атрибута _owner
    def get_owner(self):
        return self._owner  # Возвращает значение _owner (может быть строкой или None)

    # Сеттер: устанавливает новое значение владельца, только если оно является строкой
    def set_owner(self, new_owner):
        if isinstance(new_owner, str):  # Проверяем, является ли new_owner строкой
            self._owner = new_owner  # Если да — сохраняем значение в атрибут _owner
        # Если тип не строка — ничего не делаем, значение остаётся прежним (например, None)


