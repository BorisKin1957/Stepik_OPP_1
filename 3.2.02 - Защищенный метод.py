'''Задача 2: Защищенный метод
Условие:
Методы тоже могут быть защищенными, если они являются "вспомогательными" и
не предназначены для вызова извне.

Вам нужно создать класс DatabaseConnector:

В __init__ создайте публичный атрибут self.is_connected = False.

Создайте защищенный вспомогательный метод _establish_connection(),
который меняет self.is_connected на True.

Создайте публичный метод connect(), который просто вызывает
self._establish_connection().'''

class DatabaseConnector:

    def __init__(self):
        # Инициализация атрибута состояния подключения
        self.is_connected = False

    def _establish_connection(self):
        # Защищённый метод: изменяет состояние подключения на "подключено"
        self.is_connected = True

    def connect(self):
        # Публичный метод: вызывает защищённый метод для установки соединения
        self._establish_connection()


''' 
Защищённый метод _establish_connection() обозначен символом подчёркивания 
в начале имени — это соглашение в Python, указывающее, что метод не предназначен 
для прямого вызова извне класса. Вместо этого он используется внутри публичного 
метода connect().'''

