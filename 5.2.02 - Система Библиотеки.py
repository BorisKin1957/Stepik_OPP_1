'''Финальная задача №2: "Система Библиотеки"

Легенда: Вы разрабатываете простую систему для учета печатных изданий в 
библиотеке. Вам нужно создать иерархию классов для книг и журналов.

Техническое задание:

1. Базовый класс Publication (Издание):

    Атрибуты:

        title (название) — публичный.

        _author (автор/редактор) — защищенный.

        _year (год издания) — защищенный.

    Методы:

        __init__(self, title, author, year): Конструктор для установки атрибутов.

        get_info(self): Возвращает строку с базовой информацией в 
        формате: "[title]" ([author], [year]).

2. Дочерний класс Book (Книга):

    Наследование: Наследует от Publication.

    Новые атрибуты:

        isbn (ISBN код) — публичный.

    Методы:

        __init__(self, title, author, year, isbn): Конструктор, который 
        принимает isbn в дополнение к родительским атрибутам. Не забудьте 
        вызвать конструктор родителя с помощью super(), а isbn сохранить 
        в собственный атрибут.

        Переопределите метод get_info(self). Он должен расширить родительский 
        метод, добавив информацию об ISBN.

        Итоговая строка должна выглядеть так: "[title]" ([author], [year]), ISBN: [isbn].

3. Дочерний класс Magazine (Журнал):

    Наследование: Наследует от Publication.

    Новые атрибуты:

        issue_number (номер выпуска) — публичный.

    Методы:

        __init__(self, title, editor, year, issue_number): Конструктор, который
         принимает issue_number и editor (редактор).

        Важно: При вызове super().__init__ передайте editor на место аргумента 
        author (так как у журнала редактор выполняет роль автора).

        Переопределите метод get_info(self). Он должен расширить родительский 
        метод, добавив номер выпуска.

        Итоговая строка должна выглядеть так: "[title]" (Ред. [editor], [year]), 
        Выпуск №[issue_number].
'''

# Базовый класс для всех изданий
class Publication:
    # Инициализация базовых атрибутов: название, автор (защищённый), год издания (защищённый)
    def __init__(self, title, author, year):
        self.title = title            # публичное название издания
        self._author = author         # защищённый автор или редактор
        self._year = year             # защищённый год издания

    # Возвращает строку с основной информацией об издании
    def get_info(self):
        return f'"{self.title}" ({self._author}, {self._year})'


# Класс книги, наследуется от Publication
class Book(Publication):
    # Инициализация атрибутов книги: добавляется ISBN
    def __init__(self, title, author, year, isbn):
        super().__init__(title, author, year)  # вызов конструктора родительского класса
        self.isbn = isbn                       # публичный атрибут ISBN

    # Переопределение метода get_info для добавления информации об ISBN
    def get_info(self):
        return f'{super().get_info()}, ISBN: {self.isbn}'


# Класс журнала, наследуется от Publication
class Magazine(Publication):
    # Инициализация атрибутов журнала: редактор передаётся как автор, добавляется номер выпуска
    def __init__(self, title, editor, year, issue_number):
        super().__init__(title, editor, year)   # редактор используется как автор
        self.issue_number = issue_number        # публичный номер выпуска

    # Переопределение метода get_info для отображения редактора и номера выпуска
    def get_info(self):
        return (f'"{self.title}" (Ред. {self._author}, {self._year}), Выпуск '
                f'№{self.issue_number}')


# Создаем экземпляр книги
book = Book("В и мир", "Лев Толстой", 1869, "978-5-389-06254-2")

# Создаем экземпляр журнала
magazine = Magazine("National Geographic", "Сьюзан Голдберг", 2021, 8)

# Демонстрация полиморфизма: один интерфейс, разное поведение
publications = [book, magazine]
for pub in publications:
    # Для каждого объекта вызывается метод get_info(), но реализация разная
    print(pub.get_info())